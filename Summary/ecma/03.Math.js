//1.min()  出现非数字则直接返回NaN
let min=Math.min(12,8,-12,-5,25);
console.log(min);

//2.max()
let max=Math.max(12,8,-12,-5,25);
console.log(max);

//3.Math.ceil()
let ceil=Math.ceil(3.14159);
console.log(ceil);

//4.Math.floor()
let floor=Math.floor(3.14159);
console.log(floor);

//5.Math.round()
let round=Math.round(3.45)
console.log(round);

//6.Math.abs()
let abs=Math.abs(-55);
console.log(abs);

//7.Math.random()
let random=Math.random()*10;
console.log(random);




